// Helper function to get POIs by spot
export function getPOIsBySpot(spotId: string, radiusKm: number = 5): POI[] {
  const spotPrefixes: Record<string, string> = {
    'cijin': 'poi_cijin',
    'jinshan': 'poi_jinshan',
    'dulan': 'poi_dulan',
  }
  
  const prefix = spotPrefixes[spotId] || spotPrefixes['cijin']
  return mockPOIs.filter(poi => poi.id.startsWith(prefix))
}

// Helper function to get POIs by category
export function getPOIsByCategory(spotId: string, categories: string[]): POI[] {
  const allPOIs = getPOIsBySpot(spotId)
  if (categories.length === 0) return allPOIs
  return allPOIs.filter(poi => categories.includes(poi.cat))
}

